package Client;

public class Client {

  private String name;
  private int age;
  private String industry;
  private int phone_Number;
  private String issue;

  public Client (String name, int age, String industry, int phone_Number, String issue) {
    this.name = name;
    this.age = age;
    this.industry = industry;
    this.phone_Number = phone_Number;
    this.issue = issue;
  }
}
